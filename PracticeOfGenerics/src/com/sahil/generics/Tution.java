package com.sahil.generics;

public class Tution 
{
	private String tutionName;
	private String tutionAddress;
	private int tutionFee;
	
	public String getTutionName() {
		return tutionName;
	}
	public void setTutionName(String tutionName) {
		this.tutionName = tutionName;
	}
	public String getTutionAddress() {
		return tutionAddress;
	}
	public void setTutionAddress(String tutionAddress) {
		this.tutionAddress = tutionAddress;
	}
	public int getTutionFee() {
		return tutionFee;
	}
	public void setTutionFee(int tutionFee) {
		this.tutionFee = tutionFee;
	}
	
	
}
